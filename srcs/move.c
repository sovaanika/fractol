#include "../fractol.h"

void	move(int key, t_fract *f)
{
	if (key == 125)
		f->movey -= 0.1;
	else if (key == 126)
		f->movey += 0.1;
	else if (key == 123)
		f->movex += 0.1;
	else
		f->movex -= 0.1;
}
