#include "../fdf.h"

void	move(int key, t_fdf *fdf)
{
	if (key == 125)
		fdf->starty += 10;
	else if (key == 126)
		fdf->starty -= 10;
	else if (key == 123)
		fdf->startx -= 10;
	else
		fdf->startx += 10;
	draw(fdf);
}
